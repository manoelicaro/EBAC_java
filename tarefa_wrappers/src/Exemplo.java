//Criar uma classe
public class Exemplo {
    public static void main(String[] args) {
        //Criar variáveis
        int x = 10;

        //Imprimir a variável x
        System.out.println(x);

        //Criar variável
        double y = 0;

        //Converter a variável x para double
        y = (double) x;

        //Imprimir a variável z
        System.out.println(y);
    }
}
